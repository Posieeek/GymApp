Główne zasady diety low carb:

Węglowodany stanowią 10 do 15 proc. dziennej podaży energetycznej. Przypuśćmy, że bilans 
kaloryczny przykładowej osoby wynosi 2000 kcal na dobę. Z tego 10-15 proc. stanowią węglowodany. 
Daje nam to 200-300 kcal. 1 gram węglowodanów to 4 kcal. Ostatecznie wychodzi, że możemy zjeść 
50-75 gramów węglowodanów na dobę.
<br>  <br>
Odpowiednia podaż białka, które zabezpiecza masę mięśniową przed rozpadem. Osoby trenujące 
regularnie powinny spożywać 2-2,5 grama białka na kilogram masy ciała. Większa ilość będzie 
zbędna. Białko dobrej jakości dostarcza organizmowi aminokwasów, które wykorzystywane są do 
budowy komórek oraz procesów biochemicznych. Dobrym źródłem białka są jaja, które mają wysoką 
biodostępność, mięso drobiowe oraz ryby morskie.
<br>  <br>
Zdrowe tłuszcze muszą stanowić część diety low carb, ponieważ to one w głównej mierze uzupełniają
 bilans kaloryczny w trakcie dnia. Są także potrzebne do zachowania pełnej sprawności umysłowej 
 oraz układu krążenia i serca. Nasz mózg w znacznej mierze zbudowany jest z kwasów omega 3, 
 które należy regularnie dostarczać z oliwy z oliwek, orzechów, oleju lnianego, ryb morskich 
 oraz nasion. Świetnym rozwiązaniem będzie także włączenie do codziennego pożywienia kwasów 
 tłuszczowych MCT, czyli średniołańcuchowych kwasów tłuszczowych. Nasz organizm nie jest 
 w stanie ich magazynować, lecz potrafi błyskawicznie przekształcić je na energię. Źródłem 
 tych tłuszczów jest między innymi olej kokosowy.
