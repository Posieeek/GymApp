            Dieta low carb, jak sama nazwa wskazuje, bazuje na obniżeniu poziomu spożywanych 
            węglowodanów. Nie jest to ketoza, która nakazuje spożywać mniej niż 30 g węglowodanów 
            dziennie, z każdego możliwego pochodzenia. W low carb programujemy organizm 
            na czerpanie energii z zapasów tłuszczu, który chcemy spalić. Tkanka tłuszczowa 
            stanowi zabezpieczenie energetyczne organizmu. Ograniczenie węglowodanów sprawia, 
            że ciało, które potrzebuje energii, będzie sięgać właśnie do tych rezerw.
Zasady diety są bardzo proste i przejrzyste, nie oznacza to jednak, że można ją łatwo 
wprowadzić w życie. Powinno się ją stosować przez kilka, a nawet kilkanaście miesięcy. 
Organizm musi przyzwyczaić się do nowego sposobu żywienia oraz przestawić się 
na czerpanie energii w większych ilościach z kwasów tłuszczowych. Najpoważniejszym 
błędem osób próbujących odchudzić się dzięki diecie low carb jest złe wyjście
 z diety. Zaczynają one od razu jeść ilości węglowodanów jak sprzed okresu diety,
  przez co organizm szybko ponownie “łapie” tkankę tłuszczową.
