<br>
Poprawa nastroju 
Autorzy najnowszej, dietetycznej książki donoszą, że skrobia odporna znajduje się w produktach bogatych w węglowodany, takich jak banany, płatki owsiane, brązowy ryż i ziemniaki. Spożywanie tych produktów przyczynia się do tłumienia apetytu, wzmaga metabolizm, poprawia nastrój, obniża poziom stresu i przyspiesza utratę wagi. Swoje tezy naukowcy z University of Colorado Centre for Human Nutrition w Denver potwierdzają badaniami przeprowadzonymi na 4451 chętnych. Eksperyment wskazał, że najszczuplejsze osoby spożywały duże ilości węglowodanów w postaci całych ziaren, owoców i warzyw. 
<br>
<br>
Rewolucja w odchudzaniu 
Jeśli popularność nowej książki i teorii o zbawiennym wpływie węglowodanów na ciało przełoży się na popularność nowego sposobu odżywiania wielu ludzi, przyćmi to popularne w ostatnich miesiącach diety oparte na proteinach. Jedząc zgodnie z zasadami diety niskowęglowodanowej i bogatej w proteiny, oczywiście tracisz na wadze. Należy jednak pamiętać, że jest to niezdrowe. Poza tym nie jest to długotrwałe rozwiązanie - wyjaśnia Elisabeth Weichselbaum, z British Nutrition Foundation. Specjaliści przekonują, że dieta opisana w książce może przyczynić się do utraty wagi, pod jednym warunkiem. Nie będzie wymówką wymówką do spożywania gigantycznych i niekontrolowanych ilości makaronu i pizzy. 
