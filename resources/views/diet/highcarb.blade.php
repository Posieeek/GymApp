Zatem odnieśmy się do literatury naukowej. 
Najnowsza dietetyczna książka "The Carb Lover's Diet" 
("Dieta dla miłośników węglowodanów") zapewne przyniesie ulgę miłośnikom chleba, 
makaronów i pizzy. Specjaliści przekonują, że węglowodany są niezbędnym składnikiem 
w diecie zapewniającej zgrabną sylwetkę. Książka łamiąca wszelkie dietetyczne zakazy,
 w zaledwie pół roku stała się bestsellerem w Stanach. Węglowodany powracają do łask 
 w dużej mierze dzięki skrobi opornej, która nie poddaje się działaniu enzymów 
 trawiennych i nie rozkłada się do postaci glukozy. To powoduje, że nie jest ona 
 wchłaniana w jelicie cienkim, tylko przechodzi do jelita grubego, gdzie ulega 
 fermentacji z udziałem mikroflory jelitowej. W efekcie powstają krótkołańcuchowe 
 kwasy tłuszczowe, które biorą udział w metabolizmie organizmu i działają podobnie 
 jak błonnik.Skrobia oporna wypełnia przewód pokarmowy w większym stopniu niż zwykła 
 skrobia. Efektem tego jest mniejszy przyrost masy ciała, a zwłaszcza tkanki tłuszczowej. 

